
// Cookie utilities for PWA state persistence
const PWA_COOKIE_NAME = 'pwa_install_state';

export const setCookie = (name: string, value: string, days: number = 30) => {
  const expires = new Date();
  expires.setTime(expires.getTime() + (days * 24 * 60 * 60 * 1000));
  document.cookie = `${name}=${value};expires=${expires.toUTCString()};path=/;SameSite=Lax`;
};

export const getCookie = (name: string): string | null => {
  const nameEQ = name + "=";
  const ca = document.cookie.split(';');
  for (let i = 0; i < ca.length; i++) {
    let c = ca[i];
    while (c.charAt(0) === ' ') c = c.substring(1, c.length);
    if (c.indexOf(nameEQ) === 0) return c.substring(nameEQ.length, c.length);
  }
  return null;
};

export const deleteCookie = (name: string) => {
  document.cookie = `${name}=;expires=Thu, 01 Jan 1970 00:00:00 GMT;path=/`;
};

export const getPWAStateCookie = () => getCookie(PWA_COOKIE_NAME);
export const setPWAStateCookie = (value: string, days?: number) => setCookie(PWA_COOKIE_NAME, value, days);
export const deletePWAStateCookie = () => deleteCookie(PWA_COOKIE_NAME);
